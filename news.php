        <h4>Micron Publication - <a href="http://elifesciences.org/content/3/e03399">Conduit et al., eLife 2014.</a></h4>
        <img src="images/conduit1.png" alt="Conduit Live two colour" width="350" align="right" />

        <p>
          Centrosomes are cellular organelles composed of two centrioles
          surrounded by a matrix of pericentriolar material (PCM). The PCM
          expands dramatically in size in mitosis, but it is unclear how this
          process occurs. Using a new super-resolution imaging technique
          developed at Micron, Raff and colleagues now clarify how the PCM
          matures.
        </p>
        <p>
          PCM maturation is an important step in mitosis. The increase in PCM
          size is correlated with an increase in microtubule nucleation ability,
          important for the formation of the mitotic spindle and correct
          chromosome segregation. The PCM is formed by hundreds of different
          proteins, but work now published by the Raff lab (Dunn School) in
          eLife suggests that this process relies on the interplay between
          only a few proteins.
        </p>
        <p>
          Raff and colleagues had previously used a technique known as
          "Fluorescence Recovery After Photobleaching" (FRAP) to show that
          the PCM protein Cnn displayed an unusual dynamic behavior, whereby
          it was only incorporated into the central region of the PCM and then
          spread outwards. Modifying the amount of Cnn in the PCM caused a
          corresponding change in the amount of PCM, suggesting that Cnn may
          form a scaffold that spreads away from the centrioles to support the
          extended PCM structure. In this paper, Raff and colleagues used FRAP
          to show that only one other protein, Spd-2, displayed a similar
          behaviour to Cnn and could also support PCM assembly. It was
          unclear, however, how these two scaffolds related to each other,
          particularly as standard resolution microscopy could not easily
          distinguish between them.
        </p>
        <p>
          The authors therefore turned to super-resolution structured
          illumination (3D-SIM) microscopy, available at Micron. Live two
          colour 3D-SIM revealed that the Spd2 and Cnn scaffolds only
          partially overlapped. This suggested that the two proteins behave
          differently, but in what way? To address this question, Micron
          developed a new technique that combined 3D-SIM with FRAP. This
          allowed the authors to examine the dynamics of Cnn and Spd-2 in
          unprecedented detail. Remarkably, this showed a clear difference
          between the two scaffolds: Spd-2 is initially recruited around the
          wall of the centriole and then moves outwards, whereas Cnn is
          initially recruited into a broader area and then spreads further
          outwards.
        </p>
        <p>
          These observations led the authors to carry out further experiments
          that collectively suggested a simple model for PCM maturation.
          Spd-2 is recruited to the centriole wall and then helps recruit Cnn.
          Cnn is modified by the kinase Polo/Plk1 and builds a scaffold that
          supports the outward movement of Spd-2. As Spd-2 moves outwards, it
          creates a larger area for Cnn recruitment, which in turn provides
          more support for the outward movement of Spd-2. Thus, a positive
          feedback loop is formed that allows the rapid expansion of the two
          scaffolds, which recruit the rest of the PCM, during mitosis.
        </p>

