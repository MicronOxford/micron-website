        <h4>Wytham Woods Exhibition</h4>
        <p>
          Out of the Woods is an exhibition on Wytham Woods north of Oxford,
          the best studied piece of wood land in the world. The exhibition
          features prints and wood sculpture as well as photographs about
          life in the woods. We have contributed three large digital images
          that use fluorescence and DIC to capture the beauty of cells and
          organelles in bluebell leaves from Wytham Woods. The exhibition
          runs until 30 September 2012.
        </p>
        <img src="images/2_stomata.jpg"
          alt="image" width="560"align="right" />
        <h4>
          Epidermis (outer layer) of a bluebell leaf showing the guard cells
          of two stomata and red chlorophyll autofluorescence in the
          chloroplasts contained within the guard cells.
        </h4>
        <p>
          Stomata are closable pores through which gas is exchanged between
          the leaf and the environment. During the day they allow oxygen and
          water vapour to leave the leaf and air to enter while during the
          night carbon dioxide is expelled. The guard cells are the only
          cells in the epidermis that contain chloroplasts where
          photosynthesis takes place. The tissue shown in this image is in
          reality 100&#956;m x 100&#956;m (=0.1 mm x 0.1 mm) in size.
        </p>
        <img src="images/parenchyma_cell.jpg" width="560" align="right"/>
        <h4>Parenchyma (inner layer) cell of a bluebell leaf.</h4>

        <p>
          Most of the inner cells of leaves contain chloroplasts (chl) in
          the thin rim of cytoplasm around the edge of the cell. This rim
          also contains a nucleus (n, with chloroplasts in front and behind)
          and many other much smaller organelles (specialised compartments).
          The vast majority of the plant cell is made up of a vacuole (v)
          filled with water and soluble substances. The cell section in this
          image is in reality 75&#956;m (=0.075 mm) wide.
        </p>

        <img src="images/chlorophyll_autofluorescence.jpg" width="560"
          align="right"/>
        <h4>Chlorophyll autofluorescence in chloroplasts of a parenchyma cell in a bluebell leaf.</h4>

        <p>
          Chloroplasts are the compartments in a plant cell in which
          photosynthesis takes place. Photosynthesis is a process that uses
          light, water and carbon dioxide to produce sugars and oxygen.
          Without photosynthesis the earth would only be populated by
          bacteria. The network of membranes inside chloroplasts contains
          chlorophyll, which absorbs mostly blue and red light, and emits
          light in the far red part of the spectrum. The absorbed light is
          used for photosynthesis. The emitted fluorescent light from several
          chloroplasts is shown in this image. The biggest of these
          chloroplasts is 14&#956;m (=0.014 mm) in length.
        </p>

