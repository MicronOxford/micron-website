        <h3><a name="plain_english">Plain English</a></h3>

        <div id="plainenglish">
          <p>
            Micron is a community of researchers focusing on the development and
            use of state-of-the-art microscopy methods for imaging living cells.
            These interdisciplinary collaborative research groups are based in a
            number of Department in Oxford University including, the Department
            of Biochemistry, the Dunn School of Pathology, DPAG and the Wellcome
            Trust Centre for Human Genetics. Micron is funded by a major
            strategic award from Wellcome that includes staff salaries
            and large equipment purchases.
          </p>
          <p>
            Micron will focus on the use of very recently developed and emerging
            microscopy methods that allow cell biologists to image molecules in
            living cells at resolutions that exceed the conventional resolution
            limits. Imaging single molecules in living cells will allow the
            researchers to make major new discoveries and uncover the principles
            of how molecular machines function within cells.
          </p>
          <p>
            The hub of the microscopy facilities is located in the New
            Biochemistry building where many of the groups are based. Leading
            the project are Professors Neil Brockdorff, Ilan Davis and Kim
            Nasmyth from the Biochemistry department, and Professors Nick
            Proudfoot, Jordan Raff and Elizabeth Robertson, from the Dunn
            School of Pathology. They will work with colleagues from the
            Departments of Chemistry, Engineering, Physics, and Physiology,
            Anatomy and Genetics, and at hospital sites, as well as researchers
            from the hospital sites in Oxford. Each group will bring its specific
            expertise and interest.
          </p>
        </div>
