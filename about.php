        <h3>Our Vision</h3>
        <div id="vision">
          We seek to understand the fundamental biological processes taking
          place in cells using advanced bioimaging technologies to observe
          and interfer with the components of molecular machines responsible
          for cellular function.  To achieve this goal we are:

          <ol>
            <li>Creating a collaborative and multidisciplinary environment that transcends
                departmental boundaries and fosters innovative developments in microscopy,
                molecular interference and tagging.</li>

            <li>Training pre/post-doctoral researchers in the application of advanced
                imaging and related approaches, further enabling them to answer key questions
                in their field of interest.</li>

            <li>Nurturing career development for research fellows, young PIs and support staff.</li>

            <li>Disseminating newly developed methodologies to the wider scientific community and
                providing accessible information on Centre research and technology to a lay
                audience. </li>
          </ol>
        </div>

